#Function with parameters
def add(a, b):
    result = a + b
    print('Addition: ', result)

def subtract(a, b):
    result = a - b
    print('Subtraction: ', result)

def multiply(a, b):
    result = a * b
    print('Multiplication: ', result)

#Calling the functions with arguments
add(10, 5)
subtract(10, 5)
multiply(10, 5)