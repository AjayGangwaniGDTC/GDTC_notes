class Employee:
    def __init__(myself):
        myself.eno = 100
        myself.ename = 'Manoj'
        myself.sal = 45000
    def display(mydata):
        print('Employee No:', mydata.eno)
        print('Employee Name:', mydata.ename)
        print('Employee Salary:', mydata.sal)
emp = Employee()
emp.display()

